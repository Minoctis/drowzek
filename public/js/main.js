jQuery(document).ready(function () {
    
    //Manipulation des images du sous menu
    
    
    
    $(".salle-a-manger")
    
        
    .mouseenter(function () {
        $("#salle-a-manger").css("display", "block");  
    })
    .mouseleave(function (){
        $("#salle-a-manger").css("display", "none");  
    })
    
    
});